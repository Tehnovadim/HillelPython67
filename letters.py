print("Кожну другу літеру, починаючи з A:", every_second_letter)

letters_Z_to_Q_reversed = letters[-1:-10:-1]
print("Літери від Z до Q у зворотному порядку:", letters_Z_to_Q_reversed)

first_three_letters = letters[:3]
print("Перші 3 літери алфавіту:", first_three_letters)
